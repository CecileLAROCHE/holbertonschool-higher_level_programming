#!/usr/bin/python3
word = "Holberton"
word_first_3 = word[:3]      # contient les 3 premières lettres
word_last_2 = word[-2:]      # contient les 2 dernières lettres
middle_word = word[1:-1]     # contient le mot sans la première ni la dernière lettre
print(f"First 3 letters: {word_first_3}")
print(f"Last 2 letters: {word_last_2}")
print(f"Middle word: {middle_word}")
